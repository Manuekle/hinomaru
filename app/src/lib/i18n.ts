export function t(key: string, locale: Locale, vars?: Record<string, string | number>): string {
	const s = STRINGS[locale]?.[key] ?? STRINGS.en[key] ?? key;
	if (!vars) return s;
	return s.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? ''));
}
